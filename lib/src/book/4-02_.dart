import 'package:flutter/material.dart';

void main() => runApp(const MyApp());

// Основной класс приложения, наследуется от StatelessWidget (неизменяемый виджет)
class MyApp extends StatelessWidget {
  // Конструктор с ключом для идентификации виджета
  const MyApp({super.key});

  // Метод build определяет структуру пользовательского интерфейса
  @override
  Widget build(BuildContext context) {
    // MaterialApp - корневой виджет приложения, задает основные настройки
    return MaterialApp(
      title: 'Table Demo', // Заголовок приложения для системного интерфейса
      home: Scaffold(
        // Scaffold - базовая структура экрана с поддержкой Material Design
        body: Column(
          // Column - располагает дочерние элементы вертикально
          children: [
            // Container - создает пустое пространство высотой 100 пикселей
            Container(height: 100),
            // Table - виджет для создания таблицы с ячейками
            Table(
              // TableBorder - задает границы таблицы
              border: TableBorder(
                top: BorderSide(width: 2), // Верхняя граница толщиной 2 пикселя
                bottom: BorderSide(width: 2), // Нижняя граница
                left: BorderSide(width: 2), // Левая граница
                right: BorderSide(width: 2), // Правая граница
              ),
              // Список строк таблицы
              children: [
                // TableRow - строка таблицы, содержащая ячейки
                TableRow(
                  children: [
                    // Center - центрирует содержимое ячейки
                    Center(
                      // Padding - добавляет отступы внутри ячейки
                      child: Padding(
                        padding: EdgeInsets.all(10), // Отступы 10 пикселей со всех сторон
                        child: Text("1"), // Текст "1" в первой ячейке
                      ),
                    ),
                    Center(
                      child: Padding(
                        padding: EdgeInsets.all(10),
                        child: Text("2"), // Текст "2" во второй ячейке
                      ),
                    ),
                    Center(
                      child: Padding(
                        padding: EdgeInsets.all(10),
                        child: Text("3"), // Текст "3" в третьей ячейке
                      ),
                    ),
                  ],
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }
}

/*
Этот код создает простое Flutter-приложение, которое отображает экран с вертикальной колонкой (Column), содержащей:
Пустое пространство:
Пространство высотой 100 пикселей, созданное с помощью Container.

Таблица:
Таблица с одной строкой и тремя ячейками, каждая из которых содержит текст ("1", "2", "3").

Ячейки центрированы (Center), имеют отступы 10 пикселей (Padding), и таблица окружена границей толщиной 2 пикселя (TableBorder).

Приложение использует базовую структуру Material Design через MaterialApp и Scaffold. Таблица демонстрирует простую компоновку данных в виде сетки.
Методы и виджеты: За что отвечают
MaterialApp:
Корневой виджет приложения, задает основные настройки, такие как заголовок (title) и начальный экран (home).

title: отображается в системном интерфейсе (например, в панели задач).

home: задает главный экран приложения.

Scaffold:
Предоставляет базовую структуру экрана Material Design.

body: содержит основной контент экрана (в данном случае Column).

Column:
Располагает дочерние виджеты вертикально.

Используется для размещения Container и Table.

Container:
Универсальный виджет для компоновки и стилизации.

В данном случае создает пустое пространство фиксированной высоты (height: 100).

Table:
Виджет для создания табличной компоновки с рядами и столбцами.

border: задает границы таблицы через TableBorder.

children: список строк таблицы (TableRow).

TableBorder:
Определяет границы таблицы.

top, bottom, left, right: задают толщину и стиль границ (здесь width: 2).

TableRow:
Представляет строку в таблице.

children: список виджетов, представляющих ячейки строки (здесь три ячейки с текстом).

Center:
Центрирует дочерний виджет по горизонтали и вертикали внутри ячейки.

Padding:
Добавляет отступы вокруг дочернего виджета.

EdgeInsets.all(10): задает отступы 10 пикселей со всех сторон.

Text:
Отображает текст.

В данном случае используется для отображения "1", "2", "3" в ячейках таблицы.


* */