import 'package:flutter/material.dart';

void main() => runApp(const MyApp());

// Основной класс приложения, наследуется от StatelessWidget (неизменяемый виджет)
class MyApp extends StatelessWidget {
  // Конструктор с ключом для идентификации виджета
  const MyApp({super.key});

  // Метод build определяет структуру пользовательского интерфейса
  @override
  Widget build(BuildContext context) {
    // MaterialApp - корневой виджет приложения, задает основные настройки
    return MaterialApp(
      title: 'Chip Demo', // Заголовок приложения для системного интерфейса
      theme: ThemeData(
        useMaterial3: true, // Включаем Material 3 для современного дизайна (совместимо с Flutter 3.32.0)
      ),
      home: Scaffold(
        // Scaffold - базовая структура экрана с поддержкой Material Design
        body: Center(
          // Center - центрирует дочерний виджет по горизонтали и вертикали
          child: Chip(
            // Chip - виджет для отображения компактного элемента с текстом и аватаром
            avatar: CircleAvatar(
              // CircleAvatar - круглый аватар, часто используется для изображений
              backgroundImage: AssetImage("img/ron.jpg"), // Изображение из активов
            ),
            backgroundColor: Colors.grey.shade300, // Светло-серый фон чипа
            label: Text("Frank Zammetti"), // Текстовая метка чипа
          ),
        ),
      ),
    );
  }
}

/*
Этот код создает простое Flutter-приложение, которое отображает экран с одним элементом — виджетом Chip, расположенным в центре (Center). Чип представляет собой компактный элемент интерфейса, который:
Содержимое:
Слева отображается круглый аватар (CircleAvatar) с изображением из файла img/ron.jpg.

Справа отображается текст "Frank Zammetti".

Фон чипа светло-серый (Colors.grey.shade300).

Структура:
Приложение использует MaterialApp для настройки и Scaffold для базовой структуры экрана.

Center центрирует чип на экране.

Цель: Демонстрирует использование Chip для отображения компактного элемента с аватаром и текстом, часто применяемого для тегов, контактов или меток.

Методы и виджеты: За что отвечают
MaterialApp:
Корневой виджет приложения, задает настройки, такие как заголовок (title), тема (theme) и начальный экран (home).

title: отображается в системном интерфейсе (например, в панели задач).

theme: задает глобальную тему (здесь включен Material 3 через useMaterial3: true).

home: определяет главный экран приложения.

ThemeData:
Определяет тему приложения.

useMaterial3: true: включает стили Material 3, совместимые с Flutter 3.32.0.

Scaffold:
Предоставляет базовую структуру экрана Material Design.

body: содержит основной контент (здесь Center с Chip).

Center:
Центрирует дочерний виджет по горизонтали и вертикали.

Chip:
Компактный виджет для отображения метки, тега или контакта.

avatar: виджет слева от текста (здесь CircleAvatar).

backgroundColor: задает цвет фона чипа (Colors.grey.shade300).

label: текст или виджет, отображаемый в чипе (здесь Text).

CircleAvatar:
Круглый аватар, часто используется для отображения изображений или иконок.

backgroundImage: задает изображение для аватара (здесь AssetImage).

AssetImage:
Загружает изображение из активов проекта.

Требует настройки в pubspec.yaml для указания пути к файлу (img/ron.jpg).

Text:
Отображает текст в метке чипа.

* */